// let 재할당 가능 (재선언 불가)
let a;
a = 4;
console.log(a);

// const 재할당,재선언 X 무조건 초기화만!!
const b = 8;
// b = 8;
console.log(b);

// 나머지 연산자
let num1 = 7;
console.log(num1 ** 2);

// 논리 연산자 
console.log(!true);
console.log(true || false); //true일 확률을 앞쪽에 배치하는게 효율적
console.log(false && true); //false일 확률을 앞졲에 배치하는게 효율적

// 비교 연산자
console.log('' === 0);
console.log(false === null);
console.log('0' == false); //false는 0값을 가지고 있다. 
